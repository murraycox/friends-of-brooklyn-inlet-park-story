<p>
    With industry dominating the Williamsburg waterfront, manufacturers needed a way to move raw goods and products in and out of the neighborhood. With the advent of the railroad the East River became a barrier rather than a conduit. National freight lines terminated on the Hudson shore of New Jersey and the only way across was to put freight cars onto barges and float them across the harbor. In the 1870s a rail freight terminal was established at Palmer's Dock on the Northside waterfront to handle all these freight cars, expanding to become the Brooklyn Eastern District Terminal (BEDT) in 1906 (organized by the Havemeyers of Domino Sugar fame). A network of rails ran up and down Kent and Wythe avenues and many of the side streets, serving sugar refineries, breweries, barrel makers, and meat packers among many others. BEDT, operational until the 1980s, occupied the waterfront from North 6th to North 11th Street.
</p>