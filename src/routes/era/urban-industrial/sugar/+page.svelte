    The Domino sugar refinery still stands as a landmark in Williamsburg, the last remnant of an era when Williamsburg controlled 90% of the countryâ€™s sugar production. Founded in New York City by brothers William and Frederick Havemeyer in 1802, the company relocated to the Williamsburgh waterfront in 1854 to create a larger factory with direct access to the waterfront. The larger facilities and vastly expanded capacity allowed the Havemeyers to transform sugar from a luxury to a commodity and led to the creation of multiple other refineries up and down the Williamsburg waterfront, in DUMBO and even in Greenpoint. One of those refineries was that of the Dick & Meyer Sugar Company, located for many years on the East River between North 7th and North 9th Streets in what is now Marsha P. Johnson State Park.
